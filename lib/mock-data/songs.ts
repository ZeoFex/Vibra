export function getSongById(id: string): Song | undefined {
  return songs.find((s) => s.id === id);
}

export function getSongsByArtist(artistId: string): Song[] {
  return songs.filter((s) => s.artistId === artistId);
}

export function getSongsByAlbum(albumId: string): Song[] {
  return songs.filter((s) => s.albumId === albumId);
}

export function getSongsByGenre(genre: string): Song[] {
  return songs.filter((s) => s.genre.toLowerCase() === genre.toLowerCase());
}
